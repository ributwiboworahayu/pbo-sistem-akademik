    private void nipItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_nipItemStateChanged
    try {
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/polkam","root","");
           ResultSet rs = con.createStatement().executeQuery("SELECT * FROM dosen where NIP='"+nip.getItemAt(nip.getSelectedIndex())+"'");
           while(rs.next()){
               dosen.setText(rs.getString("NAMA"));
           }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Connection Error\n"+ex);
        }
    }//GEN-LAST:event_nipItemStateChanged

